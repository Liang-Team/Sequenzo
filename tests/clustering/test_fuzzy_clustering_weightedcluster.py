class TestCrispness:
    """Tests for the crispness utility function."""

    def test_crispness_normalized_range(self, seqdata_and_diss):
        """Normalized crispness is in [0, 1] for all rows."""
        _, D = seqdata_and_diss
        result = get_fuzzy_clusters(D, n_clusters=N_CLUSTERS, memb_exp=1.5, method="fanny")
        crisp = crispness(result.membership, norm=True)
        assert crisp.shape == (NROWS,)
        assert np.all(crisp >= -1e-9), "crispness must be >= 0"
        assert np.all(crisp <= 1.0 + 1e-9), "normalized crispness must be <= 1"

    def test_crispness_hard_assignment_is_one(self):
        """A hard (one-hot) membership row has normalized crispness == 1."""
        memb = np.zeros((3, 3), dtype=float)
        memb[0, 0] = 1.0
        memb[1, 1] = 1.0
        memb[2, 2] = 1.0
        crisp = crispness(memb, norm=True)
        np.testing.assert_allclose(crisp, 1.0, atol=1e-9)

    def test_crispness_uniform_is_zero(self):
        """A uniform membership row has normalized crispness == 0."""
        k = 3
        memb = np.full((5, k), 1.0 / k, dtype=float)
        crisp = crispness(memb, norm=True)
        np.testing.assert_allclose(crisp, 0.0, atol=1e-9)

    def test_crispness_unnormalized(self):
        """Unnormalized crispness equals sum of squared memberships per row."""
        memb = np.array([[0.7, 0.2, 0.1], [0.5, 0.3, 0.2]], dtype=float)
        crisp = crispness(memb, norm=False)
        expected = np.array([
            0.7**2 + 0.2**2 + 0.1**2,
            0.5**2 + 0.3**2 + 0.2**2,
        ])
        np.testing.assert_allclose(crisp, expected, atol=1e-12)

    def test_crispness_matches_weightedcluster(self, seqdata_and_diss, ref_dir_fanny):
        """Normalized crispness for FANNY k=3 matches WeightedCluster reference."""
        _, D = seqdata_and_diss
        D_ref = _load_ref_csv(ref_dir_fanny, "fanny_k3_exp15_crispness")
        if D_ref is None:
            pytest.skip("ref_fanny_k3_exp15_crispness.csv not found")
        result = get_fuzzy_clusters(D, n_clusters=N_CLUSTERS, memb_exp=1.5, method="fanny")
        crisp = crispness(result.membership, norm=True)
        ref_vals = D_ref.iloc[:, 0].to_numpy(dtype=np.float64)
        np.testing.assert_allclose(crisp, ref_vals, atol=1e-4, rtol=1e-4)


