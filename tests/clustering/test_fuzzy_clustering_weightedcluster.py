def _assert_valid_membership(memb, n, k):
    """Assert membership matrix is (n, k), row-stochastic, non-negative, finite."""
    assert isinstance(memb, np.ndarray), "membership must be np.ndarray"
    assert memb.shape == (n, k), f"Expected ({n}, {k}), got {memb.shape}"
    assert np.all(np.isfinite(memb)), "membership contains non-finite values"
    assert np.all(memb >= -1e-9), "membership must be non-negative"
    row_sums = memb.sum(axis=1)
    np.testing.assert_allclose(row_sums, 1.0, atol=1e-6, err_msg="membership rows must sum to 1")


def _compare_membership_matrices(got, ref_df, atol=1e-4, rtol=1e-4):
    """Compare Sequenzo membership (n, k) vs R reference DataFrame."""
    ref = ref_df.to_numpy(dtype=np.float64)
    assert got.shape == ref.shape, f"Shape mismatch: Sequenzo {got.shape} vs R {ref.shape}"
    # Membership columns may be permuted; align by Hungarian matching on column correlation
    k = got.shape[1]
    col_order = _match_columns(got, ref)
    ref_aligned = ref[:, col_order]
    np.testing.assert_allclose(
        got, ref_aligned, atol=atol, rtol=rtol,
        err_msg="Membership matrix differs from WeightedCluster reference"
    )


def _match_columns(a, b):
    """Match columns of b to columns of a by minimum sum-of-squared-differences."""
    from itertools import permutations
    k = a.shape[1]
    best_perm = list(range(k))
    best_cost = np.inf
    for perm in permutations(range(k)):
        cost = np.sum((a - b[:, list(perm)]) ** 2)
        if cost < best_cost:
            best_cost = cost
            best_perm = list(perm)
    return best_perm


